# Chiedo a utente numero intero, chiedere a utente un carattere, creare una stringa che sia quel carattere ripetuto tante volte quanto il numero
# Chiedi all'utente un numero intero
numero = int(input("Inserisci un numero intero: "))

# Chiedi all'utente un carattere
carattere = input("Inserisci un carattere: ")

# Crea una stringa ripetendo il carattere
stringa_ripetuta = carattere * numero

# Stampa il risultato
print(f"Risultato: {stringa_ripetuta}")